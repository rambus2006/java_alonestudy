public class ByteExample{
	public static void main(String args[]){

		//직접해보는 손코딩
		//byte 타입 변수
		byte var1=-128;
		byte var2= -30;
		byte var3=0;
		byte var4=30;
		byte var5=127;
		long var6=128L;

		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		System.out.println(var4);
		System.out.println(var5);
		System.out.println(var6);
	}
}